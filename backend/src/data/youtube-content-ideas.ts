/**
 * Obtiene ideas por canal
 */
export function getIdeasByChannel(channel: 'personal' | 'business'): VideoIdea[] {
  return contentIdeas.filter(idea => idea.targetChannel === channel);
}

/**
 * Obtiene ideas por prioridad
 */
export function getIdeasByPriority(priority: 'low' | 'medium' | 'high'): VideoIdea[] {
  return contentIdeas.filter(idea => idea.priority === priority);
}

/**
 * Obtiene ideas por categorÃ­a
 */
export function getIdeasByCategory(category: string): VideoIdea[] {
  return contentIdeas.filter(idea => idea.category === category);
}

/**
 * EstadÃ­sticas de ideas
 */
export function getContentStats() {
  const personalIdeas = getIdeasByChannel('personal');
  const businessIdeas = getIdeasByChannel('business');

  return {
    total: contentIdeas.length,
    personal: {
      count: personalIdeas.length,
      estimatedHours: personalIdeas.reduce((sum, idea) => sum + idea.estimatedDuration, 0) / 60,
    },
    business: {
      count: businessIdeas.length,
      estimatedHours: businessIdeas.reduce((sum, idea) => sum + idea.estimatedDuration, 0) / 60,
    },
    byPriority: {
      high: getIdeasByPriority('high').length,
      medium: getIdeasByPriority('medium').length,
      low: getIdeasByPriority('low').length,
    },
  };
}
